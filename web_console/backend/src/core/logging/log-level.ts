/**
 * Describes all available log levels.
 */
export enum LogLevel {
  /**
   * The trace log level.
   */
  Trace = 0,

  /**
   * The debug log level.
   */
  Debug = 1,

  /**
   * The info log level.
   */
  Info = 2,

  /**
   * The warn log level.
   */
  Warn = 3,

  /**
   * The error log level.
   */
  Error = 4,
}
